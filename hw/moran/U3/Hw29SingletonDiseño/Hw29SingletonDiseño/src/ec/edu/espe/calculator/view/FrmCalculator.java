    private void btnCalculateTotalPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateTotalPriceActionPerformed

        try {
            float price = Float.parseFloat(txtPrice.getText());

          
            float taxRate = Float.parseFloat(txtTax.getText()) / 100;
            if (taxRate < 0.01 || taxRate > 0.9) {
                txtTotal.setText("Invalid Tax Rate (1-90%)");
                return; 
            }

            taxInstance.setTaxRate(taxRate);

            float total = price + (price * taxRate);
            txtTotal.setText(Float.toString(total));
        } catch (NumberFormatException e) {
            txtTotal.setText("Invalid Input");
        }
    }//GEN-LAST:event_btnCalculateTotalPriceActionPerformed

