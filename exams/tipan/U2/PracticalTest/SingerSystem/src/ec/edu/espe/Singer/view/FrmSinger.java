    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        
        String date;
        String name;
        Float salary;
        int age;
        float year = 0;

        name = txtName.getText();
        date = formDate.format(dtcDate.getDate());
        salary = Float.parseFloat(txtSalary.getText());
        age = Integer.parseInt(txtAge.getText());

        Singer movies = new Singer(name, salary, formDate.format(dtcDate.getDate()), age,year);

        SingerController.create(SingerController.buildDocument(movies));
        Document result = SingerController.read(SingerController.buildDocument(movies));
        if (result != null) {
            year= salary*12;
            JOptionPane.showMessageDialog(null, "The total of the year:"+ year);
            JOptionPane.showMessageDialog(null, "Saved");
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrio un problema");
        }

    }//GEN-LAST:event_btnAddProductActionPerformed

