    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      try {
        int price = Integer.parseInt(jTextField4.getText());
        int smartWatchIdToUpdate = Integer.parseInt(jTextFieldId.getText()); // Obtener el ID del campo de texto
        String targetField = "Price"; 
        
        // Validar que el ID sea 56, 85 o 1
        if (smartWatchIdToUpdate == 56 || smartWatchIdToUpdate == 85 || smartWatchIdToUpdate == 1) {
            swManage.updateDocument(smartWatchIdToUpdate, targetField, price);
            JOptionPane.showMessageDialog(null, "Document updated successfully");
        } else {
            JOptionPane.showMessageDialog(null, "This Smart Watch ID cannot be updated.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Invalid format, please enter again", "Error", JOptionPane.ERROR_MESSAGE);
    }    }//GEN-LAST:event_jButton1ActionPerformed

