    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        JsonManager jsonManager = new JsonManager();
        CellPhoneController cellPhoneController = new CellPhoneController(client);
        CellPhone cellPhone = getTxtData();
        String cellPhoneInfo = jsonManager.convertToJson(cellPhone);
        cellPhoneController.insertCellPhone(cellPhoneInfo);
    }//GEN-LAST:event_btnInsertActionPerformed

