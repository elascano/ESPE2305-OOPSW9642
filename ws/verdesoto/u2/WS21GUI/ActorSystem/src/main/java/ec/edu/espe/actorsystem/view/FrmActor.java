 public void loadActorsTable() {

        CodecRegistry codecRegistry = fromRegistries(MongoClient.getDefaultCodecRegistry(),
                fromProviders(PojoCodecProvider.builder().automatic(true).build()));
        MongoDatabase db = Connection.mongodb.withCodecRegistry(codecRegistry);
        MongoCollection<Actors> collectionCustomers = db.getCollection("Actors", acttors.class);
        List<Actors> actors = collectionCustomers.find(new Document(), actors.class).into(new ArrayList<Actors>());

        Object[][] objects = new Object[actors.size()][5];

        for (int i = 0; i < actors.size(); i++) {
            objects[i][0] = actors.get(i).getId();
            objects[i][1] = actors.get(i).getName();
            objects[i][2] = actors.get(i).getAge();
            objects[i][3] = actors.get(i).get();
            objects[i][4] = actors.get(i).getAppointment();

            tableActors.setModel(new javax.swing.table.DefaultTableModel(
                    objects,
                    new String[]{
                        "Cédula", "Nombre", "Número", "Pago", "Cita", "Dirección"
                    }
            ));

        }

    }
