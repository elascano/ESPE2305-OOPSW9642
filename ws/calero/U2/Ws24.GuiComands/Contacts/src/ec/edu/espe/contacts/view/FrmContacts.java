    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        readData();
        int option = JOptionPane.showConfirmDialog(this,"Do you want to add --> " + contact, "saving warning", 2);
        if(option == 0){
            JOptionPane.showMessageDialog(rootPane, "saved");
            //call to hod to save in MongoDB
            clearFields();
        }
        if(option == 1){
            JOptionPane.showMessageDialog(rootPane, "not Saved");
            clearFields();
        }
        if(option == 2){
            JOptionPane.showMessageDialog(rootPane, "Canceed");
        }
                
        clearFields();
        
    }//GEN-LAST:event_btnAddActionPerformed

