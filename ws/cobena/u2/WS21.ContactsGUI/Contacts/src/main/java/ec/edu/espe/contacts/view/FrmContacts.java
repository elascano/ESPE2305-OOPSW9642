    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        readContactData();
        int option = JOptionPane.showConfirmDialog(this, "Do you really want to add: \n" + contact.toString());
        if(option == 0){
            JOptionPane.showMessageDialog(rootPane, "Succesfully Saved");
            //TODO call MongoDB controller to save the contact
            clearFields();
        }
        else if(option == 1){
            JOptionPane.showMessageDialog(rootPane, "Not saved, clearing files");
            clearFields();
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Canceled");
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

